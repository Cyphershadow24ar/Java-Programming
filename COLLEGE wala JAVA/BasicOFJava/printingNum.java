
public class printingNum {
    public static void main(String[] args) {
        System.out.println("4"); // -> 4
        System.out.println(3); // -> 3 
        System.out.println("4+3"); // -> 4+3
        System.out.println(4+3); // -> 7
        System.out.println("4" + "3"); // -> 43  
    }
}
