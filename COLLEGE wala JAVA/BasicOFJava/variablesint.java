public class variablesint {
    public static void main (String[] args){
        int x;
        x = 26+37;  // stores the value of 26+37 in x.
        System.out.println(x);
        x = 400;    // now value changes and then prints the new value.
        System.out.println(x);
        x = 4; 
        System.out.println(x+1); // here x+1 means-> 4+1 = 5
        x = x + 5; // here x = 4+5 = 9 , x ki existing value mai 5 add kardo !
        System.out.println(x); 
        x = 500;
        x = x - 10;
        System.out.println(x); // 500-10 = 490

        int y = 5;
        System.out.println(y);
        y = 7;
        System.out.println(y);
        y = y + 6;
        System.out.println(y);
        y = y -20;
        System.out.println(y); 
    }
}
